// Classificador heurístico — atribui categoria + local com base no nome e grupo
function classify(nome, grupo){
  const u = nome.toUpperCase();
  let category, local;

  if(grupo === 'M'){
    // Condimentos
    if(/MOLHO|KETCHUP|MOSTARDA|MAIONESE|AZEITE|^OLEO|AÇAFRÃO|ACAFRAO|PIMENTA|TEMPERO|^SAL\b|AMACIANTE|^ALHO|VINAGRE|SHOYU|PÁPRICA|PAPRICA|HONDASHI|CHIMICHURRI|MARGARINA/.test(u))
      return { category:'Condimentos', local:'condimentos' };
    if(/LEITE INTEGRAL|MANTEIGA|CREME DE LEITE/.test(u))
      return { category:'Frios', local:'frios' };
    if(/BATATA CONG|PAO DE ALHO|CONG\b/.test(u))
      return { category:'Mercearia', local:'camara-congelados' };
    if(/CARVAO|CARVÃO/.test(u))
      return { category:'Insumos', local:'estoque-cozinha' };
    if(/PORTA  ADOCANTE|PAPEL TOALHA/.test(u))
      return { category:'Materiais', local:'estoque-cozinha' };
    return { category:'Mercearia', local:'mercearia' };
  }

  if(grupo === 'CR'){
    if(/CONG|CAMARAO|KAFTA|BARRIGUINHA|TOUCINHO|COSTELINHA SERRADA/.test(u))
      return { category:'Carnes', local:'camara-congelados' };
    return { category:'Carnes', local:'camara-resfriada' };
  }

  if(grupo === 'FR'){
    if(/BOLINHO|CROQUETA|PASTEL|GATEAU|KIBE|CAMAFEU/.test(u))
      return { category:'Frios', local:'camara-congelados' };
    if(/PORTA COMANDA/.test(u))
      return { category:'Materiais', local:'outros' };
    if(/PICANHA MONTADA/.test(u))
      return { category:'Carnes', local:'camara-resfriada' };
    return { category:'Frios', local:'frios' };
  }

  if(grupo === 'BB'){
    if(/WHISKY|VODKA|^GIN |GIN BEEFEATER|GIN TANQUERAY|TEQUILA|CACHACA|CACHAÇA|^RUM|VERMOUTH|CAMPARI|APEROL|LICOR|BITTER|ABSOLUT|ORLOFF|SMIRNOFF|JACK DANIELS|JOHNNIE|BUCHANANS|OLD PARR|PIRASSUNUNGA|BACARDI|CUERVO|MARTINI/.test(u))
      return { category:'Destilados', local:'estoque-bar' };
    if(/CHOPP|CERV|HEINEKEN|BRAHMA|ANTARCTICA|BUDWEISER|CORONA|SPATEN|STELLA|MICHELOB|^ORIGINAL|AMSTEL|CERVEGELA|BARRIL/.test(u))
      return { category:'Bebidas', local:'camara-bar' };
    if(/COCA|GUARANA|PEPSI|H2OH|TONICA|RED BULL|GATORADE|AGUA MIN|^REF |REFRI|51 ICE|BEATS/.test(u))
      return { category:'Bebidas', local:'camara-bar' };
    if(/POLPA|ABACAXI|ACEROLA|MANGA |MARACUJÁ|MARACUJA|UVA |FRUTAS VERMELHAS|MORANGO |FRUTA POLPA|HORTELÃ|PEQUI/.test(u))
      return { category:'Insumos', local:'freezer-bar' };
    if(/VINHO|RESERVADO|COQUETEL|PARATUDO|XAROPE|SUCO/.test(u))
      return { category:'Bebidas', local:'estoque-bar' };
    if(/CHOPEIRA|ESPREMEDOR|EXTRATOR|COQUETELEIRA|DOSADOR|GAS PARA|CAPSULAS|GARRAFA |CAIXA PLAST|GARRAFEIRA|CARAJILLO|COPO DE VIDRO/.test(u))
      return { category:'Materiais', local:'estoque-bar' };
    return { category:'Bebidas', local:'estoque-bar' };
  }

  if(grupo === 'MT'){
    if(/AVENTAL|CAMISA|GOLA|BANDANA|CALCA|DOMA|TOUCA|SAPATO|JAPONA|LUVA/.test(u))
      return { category:'Uniforme', local:'outros' };
    if(/SABONETE|DESINFETANTE|SABONETEIRA|PAPEL HIG|TOALHEIRO|CESTO DE LIXO|LIXEIRA|TRIEL|PAPEL TOALHA|GUARDANAPO/.test(u))
      return { category:'Limpeza', local:'estoque-cozinha' };
    if(/COPO|TACA|TIGELA|JARRA|CANECA |CANECAO|FRASCO POURER|DOSADOR|PORTA CHOPP/.test(u))
      return { category:'Utensílios', local:'estoque-bar' };
    if(/EXTINTOR|FURADEIRA|MAQUINA DE GELO|SENSOR/.test(u))
      return { category:'Equipamentos', local:'outros' };
    return { category:'Utensílios', local:'estoque-cozinha' };
  }

  if(grupo === 'CX'){
    if(/ALCOOL/.test(u)) return { category:'Limpeza', local:'salao' };
    return { category:'Caixa', local:'salao' };
  }

  return { category:'Outros', local:'outros' };
}

// Gera array final de produtos com sku auto-gerado e classificação aplicada
window.SEED_PRODUTOS = RAW.map((row, idx) => {
  const [name, unit, avgCost, grupo] = row;
  const { category, local } = classify(name, grupo);
  const id = 'PROD-' + String(idx + 1).padStart(4, '0');
  // SKU: 3 letras da categoria + sequencial
  const catPrefix = category.replace(/[^A-Z]/g,'').substring(0,3).padEnd(3,'X') || 'OUT';
  const sku = `${catPrefix}-${String(idx + 1).padStart(4, '0')}`;
  return {
    id, sku, name, category, local, unit,
    minStock: 0,
    currentStock: 0,
    avgCost,
    salePrice: 0,
    createdAt: new Date().toISOString()
  };
});

